    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if(textFieldCedula.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo cédula no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldCedula.requestFocus();
            return;
        }
        if(textFieldTipoCedula.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo tipo cédula no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldTipoCedula.requestFocus();
            return;
        }
        if(textFieldNombre.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo nombre no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldNombre.requestFocus();
            return;
        }
        if(textFieldApellidos.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo apellidos no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldApellidos.requestFocus();
            return;
        }
        if(textFieldEstadoCivil.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo estado civil no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldEstadoCivil.requestFocus();
            return;
        }
        if(textFieldSexo.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo sexo no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldSexo.requestFocus();
            return;
        }
        if(textFieldDireccion.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo direccion no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldDireccion.requestFocus();
            return;
        }
         if(textFieldTelefono.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo telefono no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldTelefono.requestFocus();
            return;
        }
         if(textFieldFechaNacimiento.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null,"El campo fecha nacimiento no debe ser vacío", "Warning", JOptionPane.WARNING_MESSAGE);
            textFieldFechaNacimiento.requestFocus();
            return;
        }
         Funcionario funcionario = new Funcionario();
         funcionario.setId(Integer.valueOf(textFieldCedula.getText().trim()));
         funcionario.setTipoId(textFieldTipoCedula.getText().trim());
         funcionario.setNombre(textFieldNombre.getText().trim());
         funcionario.setApellido(textFieldApellidos.getText().trim());
         funcionario.setEstadoCivil(textFieldEstadoCivil.getText().trim());
         funcionario.setSexo(textFieldSexo.getText().trim().charAt(0));
         funcionario.setDireccion(textFieldDireccion.getText().trim());
         funcionario.setTelefono(textFieldTelefono.getText().trim());
         funcionario.setFechaNacimiento(LocalDate.parse(textFieldFechaNacimiento.getText().trim()));
         textFieldCedula.setText("");
         textFieldTipoCedula.setText("");
         textFieldNombre.setText("");
         textFieldApellidos.setText("");
         textFieldEstadoCivil.setText("");
         textFieldSexo.setText("");
         textFieldDireccion.setText("");
         textFieldTelefono.setText("");
         textFieldFechaNacimiento.setText("");
         funcionarioController.createFuncionario(funcionario);
         listarFuncionarios();
        
        
        
        
    }//GEN-LAST:event_btnCrearActionPerformed

