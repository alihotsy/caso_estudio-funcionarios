    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(textFieldCedulaEdit.getText().trim().length() == 0){
            JOptionPane.showMessageDialog(null, "Debe seleccionar a un funcionaro", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Integer idFuncionario = Integer.valueOf(textFieldCedulaEdit.getText().trim());
        boolean funcionarioEliminado = funcionarioController.deleteFuncionario(idFuncionario);
        if(!funcionarioEliminado){
            JOptionPane.showMessageDialog(null, "No se pudo eliminar al usuario con ID = "+ idFuncionario, "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        JOptionPane.showMessageDialog(null,"Usuario eliminado satisfactoriamente!","SUCCESS", JOptionPane.INFORMATION_MESSAGE);
        listarFuncionarios();
        textFieldCedulaEdit.setText("");
         textFieldTipoCedulaEdit.setText("");
         textFieldNombreEdit.setText("");
         textFieldApellidosEdit.setText("");
         textFieldEstadoCivilEdit.setText("");
         textFieldSexoEdit.setText("");
         textFieldDireccionEdit.setText("");
         textFieldTelefonoEdit.setText("");
         textFieldFechaNacimientoEdit.setText("");
    }//GEN-LAST:event_btnEliminarActionPerformed

