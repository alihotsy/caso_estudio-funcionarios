    private void listarFuncionarios() {
        jComboBoxFuncionarios.removeAllItems();
        
        Funcionario funcionario = new Funcionario();
        funcionario.setNombre(SELECCIONAR);
        funcionario.setId(0);
        jComboBoxFuncionarios.addItem(funcionario);
        
        
        DefaultTableModel defaultTableModel = new DefaultTableModel();
        
        
        
        List<Funcionario> funcionarios = funcionarioController.showFuncionarios();
        
        if(funcionarios.isEmpty()){
            JOptionPane.showMessageDialog(null, "No hay funcionarios para mostrar :(","Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for(String columna : COLUMNAS){
          defaultTableModel.addColumn(columna);
        }
        
        jTableFuncionarios.setModel(defaultTableModel);
        
        defaultTableModel.setRowCount(funcionarios.size());
        
        
        for(int i=0;i< funcionarios.size(); i++ ){
            defaultTableModel.setValueAt(funcionarios.get(i).getId(), i, 0);
            defaultTableModel.setValueAt(funcionarios.get(i).getTipoId(), i, 1);
            defaultTableModel.setValueAt(funcionarios.get(i).getNombre(), i, 2);
            defaultTableModel.setValueAt(funcionarios.get(i).getApellido(), i, 3);
            defaultTableModel.setValueAt(funcionarios.get(i).getEstadoCivil(), i, 4);
            defaultTableModel.setValueAt(funcionarios.get(i).getSexo(), i, 5);
            defaultTableModel.setValueAt(funcionarios.get(i).getDireccion(), i, 6);
            defaultTableModel.setValueAt(funcionarios.get(i).getTelefono(), i, 7);
            defaultTableModel.setValueAt(funcionarios.get(i).getFechaNacimiento(), i, 8);
            jComboBoxFuncionarios.addItem(funcionarios.get(i));
        }
    }
    
    private void addListener(){
        jComboBoxFuncionarios.addItemListener(event -> {
            Funcionario funcionarioSelected = (Funcionario) event.getItem();
            if(funcionarioSelected.toString().equals("0 "+SELECCIONAR)){
                textFieldCedulaEdit.setText("");
                textFieldTipoCedulaEdit.setText("");
                textFieldNombreEdit.setText("");
                textFieldApellidosEdit.setText("");
                textFieldEstadoCivilEdit.setText("");
                textFieldSexoEdit.setText("");
                textFieldDireccion.setText("");
                textFieldTelefono.setText("");
                textFieldFechaNacimientoEdit.setText("");
                return;
            }
            textFieldCedulaEdit.setText(funcionarioSelected.getId().toString());
            textFieldTipoCedulaEdit.setText(funcionarioSelected.getTipoId());
            textFieldNombreEdit.setText(funcionarioSelected.getNombre());
            textFieldApellidosEdit.setText(funcionarioSelected.getApellido());
            textFieldEstadoCivilEdit.setText(funcionarioSelected.getEstadoCivil());
            textFieldSexoEdit.setText(funcionarioSelected.getSexo().toString());
            textFieldDireccionEdit.setText(funcionarioSelected.getDireccion());
            textFieldTelefonoEdit.setText(funcionarioSelected.getTelefono());
            textFieldFechaNacimientoEdit.setText(funcionarioSelected.getFechaNacimiento().toString());
            
            
            
        });
    }
